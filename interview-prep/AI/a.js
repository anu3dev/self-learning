/**
 * Evolution of generative AI -
 * 
 * 
 * Gen AI is subset of Deep Learning, which is a subset of Machine Learning, which is a subset of AI.
 * 
 * 
 * Generative AI is a type of AI that can create new content, such as text, images, or music, based 
 * on the data it has been trained on. It uses deep learning techniques to generate new data that 
 * resembles the training data. 
 * 
 * 
 * Generative AI models, such as GPT-3, DALL-E, and others, are trained on large datasets and can produce 
 * human-like text, realistic images, and even music compositions. These models learn patterns and 
 * structures from the training data and can generate new content that is coherent and contextually relevant.
 *
 * 
 * Deep Learning is a subset of Machine Learning that uses neural networks with many layers (hence "deep")
 * to analyze various factors of data. It is particularly effective for tasks such as image and speech
 * recognition, natural language processing, and more.
 * 
 * 
 * Machine Learning is a broader field that encompasses various algorithms and techniques for training
 * models to make predictions or decisions based on data. It includes supervised learning, unsupervised 
 * learning, reinforcement learning, and more.
 */