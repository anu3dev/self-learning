'''
Assignment in Python
'''

# Basic assignment:
x = 10

# Multiple assignment:
a, b = 5, 10

# Chain assignment:
x = y = z = 100

# Augmented assignment:
x += 5
x -= 2
x *= 3



'''
Delimiter in Python

Delimiter = character that separates values.
'''

# In split()
s = "a,b,c"
print(s.split(","))   # , is delimiter

# In join()
l = ["a","b","c"]
print("-".join(l))    # - is delimiter