'''
Artificial Intelligence (AI): A broad field of computer science that focuses on creating systems capable of performing tasks that 
usually require human intelligence, such as reasoning and decision-making.

Machine Learning (ML): A subset of AI that enables systems to learn from data and improve their performance without being explicitly programmed.

Neural Networks: A type of machine learning model inspired by the human brain, designed to recognize patterns and process complex information.

Deep Learning: An advanced branch of machine learning that uses multi-layered neural networks to extract high-level features from large volumes of data.

Generative AI: A specialized area of AI that generates new content—such as text, images, audio, or code—based on patterns learned from existing data.


Artificial Intelligence ---> Machine Learning ---> Deep Learning ---> Generative AI
'''



